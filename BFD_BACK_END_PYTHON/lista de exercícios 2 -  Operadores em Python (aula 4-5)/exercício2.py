# Entrada de dados
A = float(input("Digite o primero número (A): "))
B = float(input("Digite outro número (B): "))

# Operações
soma = A + B
subtracao = A - B
multiplicacao = A * B
divisao = A / B 

# Saída de dados
print("A soma entre A e B é:", soma,
      "\nA subtração entre A e B é:", subtracao,
      "\nA multiplicação entre A e B é:", multiplicacao,
      "\nA divisão entre A e B é:", divisao)

