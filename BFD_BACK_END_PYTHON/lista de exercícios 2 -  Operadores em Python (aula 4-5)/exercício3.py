# Entrada de dados
valor_hora = float(input("Valor recebido por hora: "))
horas_mes = float(input("Número de horas trabalhadas no mês: "))

# Cálculo 
salario_mes = valor_hora * horas_mes

# Saída
print(f"Seu salário este mês será de: R$", salario_mes)

