# Entrada de dados
num1 = int(input("Insira o primeiro valor inteiro: "))
num2 = int(input("Insira o segundo valor inteiro: "))

# Processamento
POTENCIA = num1 ** num2      # num1 elevado a num2
DIVISAO_INTEIRA = num1 // num2     # divisão inteira de num1 por num2

# Saída de dados
print(f"{num1} ** {num2} = {POTENCIA} ; {num1} // {num2} = {DIVISAO_INTEIRA}")

# Saída esperada:
# num1 ** num2 = potencia ; num1 // num2 = divisao_inteira
