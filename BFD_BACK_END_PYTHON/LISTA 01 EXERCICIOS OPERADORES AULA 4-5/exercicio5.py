# Entrada de dados (declaração da variável estoque)
estoque = 50
valor = int(input("Quantidade adicionada ao estoque: "))

# Mostra o valor inicial do estoque
print(f"Estoque inicial:", estoque)

# Atualiza o estoque usando o operador de atribuição de adição
estoque += valor
print(f"Estoque final:", estoque)

