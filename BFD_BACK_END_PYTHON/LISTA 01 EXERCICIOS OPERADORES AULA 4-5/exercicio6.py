# Entrada de dados
numero = 2
expoente = int(input("Adicione o valor do expoente: "))

# Mostra o valor inicial
print(f"Número inicial:", numero)

# Atualiza o número elevando ao expoente informado mostrando o valor final 
numero **= expoente
print(f"Número final:", numero)

# Saída esperada: 
# Número inicial: valor 
# Número final: valor
