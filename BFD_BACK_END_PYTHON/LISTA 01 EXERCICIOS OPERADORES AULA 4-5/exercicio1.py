# Entrada de dados
num1 = int(input("Digite um valor: "))
num2 = int(input("Digite outro valor: "))

# Processamento
SOMA = num1 + num2
SUBTRACAO = num1 - num2

# Saída de dados
print(f"{num1} + {num2} = {SOMA}")
print(f"{num1} - {num2} = {SUBTRACAO}")

