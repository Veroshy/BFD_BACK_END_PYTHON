# utilizar o "int" em comandos que precisam ser somados como palavras utiliza os seusdados para numeros
# exemplo: colocando apenas num_1 + num_2 sem colocar o int antes do input vai somar as nomeclaturas

num1 =  int(input("digite um valor"))
num2 =  int(input("digite um valor"))

print(f"num1 + num2 = {num1 + num2}")
print(f"num1 - num2 + {num1 - num2}") 